public class Array_constrains {

	public static void main(String[] args) {
		
		int numb [] = new int[5];
		
		numb[0] = 1749;
		numb[1] = 2002;
		numb[2] = 1988;
		numb[3] = 1496;
		numb[4] = 2035;
		
		for (int i = 0 ; i< numb.length; i ++)
		{
			if (numb[i] == 1988)
			{
				System.out.println("True");
			}
			else
			{
				System.out.println("False");
			}
	}
	}
	}